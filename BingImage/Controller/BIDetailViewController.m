#import "BIDetailViewController.h"
#import <UIImageView+WebCache.h>

@interface BIDetailViewController ()

@end

@implementation BIDetailViewController

- (void)viewDidLoad {
    [super viewDidLoad];
    
    self.view.backgroundColor = [UIColor whiteColor];
    
    UIImageView *imageView = [[UIImageView alloc] initWithFrame:CGRectMake(0, 0, self.view.frame.size.width, self.view.frame.size.height)];
    [imageView sd_setImageWithURL:[NSURL URLWithString:self.imageData.original_pic] placeholderImage:[UIImage imageNamed:@"loading"]];
    [self.view addSubview:imageView];
    
}

- (void)didReceiveMemoryWarning {
    [super didReceiveMemoryWarning];
    
}

